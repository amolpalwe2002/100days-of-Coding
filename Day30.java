//Program to concatenate a String 

import java.util.*;

public class Day30 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        String str1 = sc.nextLine();
        String str2 = sc.nextLine();

        System.out.println(str1.concat(str2));


        sc.close();
    }
    
}
